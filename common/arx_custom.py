def rotate_left(val, n):
    """执行32位循环左移"""
    return ((val << n) | (val >> (32 - n))) & 0xFFFFFFFF


def to_signed_32(val):
    """将无符号32位整数转换为有符号32位整数"""
    if val > 0x7FFFFFFF:
        return val - 0x100000000
    return val


def to_unsigned_32(val):
    """将整数强制转换为无符号32位整数"""
    return val & 0xFFFFFFFF


